#![no_std] // Не используем стандартную библиотеку
#![no_main] // Не используем стандартную точку входа

use my_os::{println, print}; // Импортируем только необходимые функции

/// Эта функция вызывается при запуске ядра
#[no_mangle]
pub extern "C" fn _start() -> ! {
    // Инициализируем систему
    my_os::init();
    
    // Выводим приветствие
    println!("Добро пожаловать в MyOS!");
    println!("Введите 'help' для получения списка команд");
    print!("> ");
    
    // Главный цикл ОС - просто ждем прерываний
    loop {
        x86_64::instructions::hlt();
    }
}